                <p>JAKARTA, KOMPAS.com - Laporan Organisasi untuk Kerja Sama Ekonomi dan Pembangunan (Organization for Economic Cooperation and Development) mengungkapkan, pandemi Covid-19 telah menghancurkan 22 juta pekerjaan di negara-negara kaya. 
                    Organisasi yang berbasis di Paris ini mengingatkan munculnya risiko peningkatan angka pengangguran jangka panjang akibat pandemi Covid-19. Berdasarkan laporan itu, pekerjaan di negara-negara OECD diperkirakan tidak akan pulih ke tingkat pra-pandemi sebelum 2023.
                    "Pemulihan ekonomi yang kuat yang sedang berlangsung di negara-negara OECD, belum sepenuhnya diterjemahkan ke dalam pekerjaan baru yang cukup untuk mengembalikan tingkat pekerjaan ke tingkat pra-pandemi di sebagian besar ekonomi anggota," tulis laporan itu, sebagaimana dikutip CNN, Jumat (9/7/2021). 
                    Laporan memperkirakan, pandemi telah membengkakkan peringkat pengangguran di negara-negara OECD dengan total lebih dari 8 juta pada tahun 2020. Sebanyak 14 juta orang lainnya tidak aktif mencari pekerjaan.Kemudian, pada akhir 2020, jumlah orang yang menganggur melonjak sebesar 60 persen di atas tingkat pra-pandemi.
                    Angka tersebut terus tumbuh pada kuartal I tahun 2021 dan secara tidak proporsional mempengaruhi kelompok yang sudah rentan.<br>
