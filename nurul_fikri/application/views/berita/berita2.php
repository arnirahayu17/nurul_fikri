                <p>KOMPAS.com - Pada 2025 mendatang, World Economic Forum memperkirakan ada 85 juta pekerjaan yang selama ini ditangani tenaga manusia yang akan digantikan oleh mesin. Selain itu, dalam laporan The Future of Jobs Report 2020, diperkirakan ada 97 juta pekerjaan baru yang akan muncul, di mana peran tersebut akan lebih disesuaikan pada kombinasi antara tenaga manusia, mesin, dan algoritma. Dalam lima tahun terakhir, WEF juga meneliti pekerjaan apa yang kira-kira banyak dibutuhkan hingga 5 tahun ke depan, termasuk di bidang informasi dan teknologi (IT)
                Ada beberapa pekerjaan di bidang informasi dan teknologi (IT) yang diprediksi akan terus meningkat, seperti analis data, ilmuwan, spesialis AI dan machine learning, teknisi robotik, pengembang software dan aplikasi, dan juga spesialis transformasi digital. Pekerjaan seperti spesialis otomasi proses, analis keamanan informasi Internet of Things (IoT), juga tercatat mulai muncul dan semakin banyak dibutuhkan
                Di beberapa industri khusus juga muncul beberapa peran baru. Seperti teknisi material di sektor otomotif, spesialis e-commerce dan sosial media di sektor konsumer, teknisi energi terbarukan di sektor energi
                Selain itu ada pula teknisi teknologi finansial (tekfin) di sektor layanan keuangan, ahli biologi dan genetika di bidang kesehatan, dan juga ilmuwan penginderaan jauh serta teknisi pertambangan dan logam.
                Sebaliknya, pada tahun 2025 beberapa peran diprediksi akan mulai kurang dibutuhkan. Beberapa pekerjaan itu di antaranya adalah entri data panitera, sekretaris administrasi dan eksekutif. Selain itu ada pula panitera akuntansi pembukuan dan penggajian, akuntan dan auditor, buruh pabrik, layanan bisnis dan manajer administrasi. Munculnya peran-peran baru itu, dikutip KompasTekno dari WEForum, Senin (21/6/2021), mencerminkan percepatan otomatisasi sekaligus peningkatan risiko keamanan siber. Adopsi teknologi merupakan keniscayaan dan sulit terelakkan. Kendati demikian, teknologi tidak serta merta menggantikan peran manusa sepenuhnya. Justru, teknologi tetap memunculkan beberapa peluang pekerjaan baru seperti yang dipaparkan di atas.<br>
